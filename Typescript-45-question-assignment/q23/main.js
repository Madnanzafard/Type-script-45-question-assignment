var car = "Toyota";
console.log(car == "Toyota");
console.log(car === "Toyota");
console.log(car != "Toyota");
console.log(car !== "Toyota");
console.log(car == "Suzuki");
console.log(car === "Suzuki");
console.log(car != "Suzuki");
console.log(car !== "Suzuki");
console.log(car.length > 3);
